#include "libft.h"

int	ft_ilen(long long int n, int base)
{
	int	i;

	i = 0;
	if (n == 0)
		return (1);
	if (n < 0 && base == 10)
		i++;
	while (n != 0)
	{
		n /= base;
		i++;
	}
	return (i);
}
