#include "libft.h"

int	ft_is_separator(char c)
{
	if (c == '\t' || c == ' ')
		return (1);
	else
		return (0);
}

int	ft_isword(char c)
{
	if (c == '\0' || c == '\t' || c == ' ' || c == '\n' || c == '\0')
		return (0);
	else
		return (1);
}
